/**
 * Repeatable HTTP-benchmark aggregator. Runs the full `run.ts` matrix N times in fresh subprocesses
 * (so each run is independent — no JIT carryover), takes the **median across runs** for every
 * framework × workload cell, and regenerates the results tables in BENCHMARKS.md between markers.
 *
 * The single sample is already a median-of-3 oha runs; this medians *across whole-matrix runs* on top,
 * which is what tames the box's run-to-run swing. Read the same-run ratios, not the absolutes.
 *
 *   bun run bench/http/aggregate.ts                  # 5 runs, print tables to stdout
 *   bun run bench/http/aggregate.ts --runs 7 --write # 7 runs, rewrite BENCHMARKS.md in place
 *   bun run bench/http/aggregate.ts node --runs 3    # one section only (bun | node | deno)
 *
 * Emits only tool versions + run knobs + measured numbers — no host/device/path info.
 */
import { dirname, join } from "node:path"
import { fileURLToPath } from "node:url"
import { httpSliceFromNode, writeSiteBench } from "../site-bench.ts"

const HERE = dirname(fileURLToPath(import.meta.url))
const RUN = join(HERE, "run.ts")
const BENCHMARKS = join(HERE, "..", "..", "BENCHMARKS.md")

// BENCHMARKS.md is NOT committed (numbers move with machine load — see bench/README.md). On a fresh
// clone `--write` has no file to splice into, so create a skeleton with the markers it expects.
const BENCHMARKS_TEMPLATE = `# Benchmarks — local results (not committed)

Generated by \`bun run bench:http:update\`. Numbers move with machine load — read same-run ratios,
not absolutes. See [bench/README.md](bench/README.md).

<!-- BENCH:META:START -->
<!-- BENCH:META:END -->

## bun

<!-- BENCH:bun:START -->
<!-- BENCH:bun:END -->

## node

<!-- BENCH:node:START -->
<!-- BENCH:node:END -->

## deno

<!-- BENCH:deno:START -->
<!-- BENCH:deno:END -->
`

interface Measure {
  readonly rps: number
  readonly p50ms: number
  readonly p99ms: number
}
interface Meta {
  readonly bun: string
  readonly node: string
  readonly deno: string
  readonly oha: string
  readonly runs: number
  readonly durationS: number
  readonly connections: number
}
type Results = Record<string, Record<string, Record<string, Measure>>>

// Must match run.ts's selected workloads (default = core read + write; `--full` = all four).
const FULL = process.argv.includes("--full")
const WORKLOADS: readonly string[] = FULL
  ? ["GET /", "GET /users/:id", "GET /search?query", "POST /users"]
  : ["GET /users/:id", "POST /users"]
// Canonical row order per section (ceiling last in each list is fine — rows are re-sorted by rps).
const FRAMEWORKS: Record<string, readonly string[]> = {
  bun: ["nifra", "elysia", "hono", "bun-raw", "bun-native"],
  node: ["nifra", "hono", "fastify", "express", "elysia", "node-raw"],
  deno: ["nifra", "hono", "elysia", "deno-raw"],
}
const CEILING: Record<string, string> = {
  bun: "bun-native",
  node: "node-raw",
  deno: "deno-raw",
}

function parseArgs(argv: readonly string[]): { runtime?: string; runs: number; write: boolean } {
  const write = argv.includes("--write")
  const runsIdx = argv.indexOf("--runs")
  const runsRaw = runsIdx >= 0 ? argv[runsIdx + 1] : undefined
  const runs = runsRaw !== undefined && /^\d+$/.test(runsRaw) ? Number(runsRaw) : 5
  const runtime = argv.find((a, i) => !a.startsWith("--") && argv[i - 1] !== "--runs")
  return runtime !== undefined ? { runtime, runs, write } : { runs, write }
}

function median(nums: number[]): number {
  if (nums.length === 0) return 0
  const sorted = [...nums].sort((a, b) => a - b)
  return sorted[sorted.length >> 1] ?? 0
}

async function runOnce(runtime: string | undefined): Promise<{ meta: Meta; results: Results }> {
  const args = ["run", RUN]
  if (runtime !== undefined) args.push(runtime)
  args.push("--json")
  if (FULL) args.push("--full") // forward so run.ts emits the same workload set aggregate expects
  const proc = Bun.spawn(["bun", ...args], { stdout: "pipe", stderr: "inherit" })
  const [out, code] = await Promise.all([new Response(proc.stdout).text(), proc.exited])
  if (code !== 0) throw new Error(`run.ts exited ${code}`)
  // run.ts prints any per-target errors to stderr; the JSON is the last non-empty stdout line.
  const line = out.trim().split("\n").filter(Boolean).at(-1) ?? ""
  return JSON.parse(line) as { meta: Meta; results: Results }
}

function fmt(n: number): string {
  return Math.round(n).toLocaleString("en-US")
}

/** Render one workload's table, rows sorted by rps desc, nifra bolded, vs the runtime ceiling. */
function table(runtime: string, workload: string, cells: Record<string, Measure>): string {
  const ceilRps = cells[CEILING[runtime] ?? ""]?.rps ?? 0
  const rows = (FRAMEWORKS[runtime] ?? Object.keys(cells))
    .map((f) => ({ f, m: cells[f] ?? { rps: 0, p50ms: 0, p99ms: 0 } }))
    .filter((r) => r.m.rps > 0)
    .sort((a, b) => b.m.rps - a.m.rps)
  const head = `| \`${workload}\` | req/s | p50 | p99 | % of ceiling |\n| --- | ---: | ---: | ---: | ---: |`
  const body = rows
    .map(({ f, m }) => {
      const pct = ceilRps > 0 ? `${Math.round((m.rps / ceilRps) * 100)}%` : "—"
      const cellVals = [
        f,
        `${fmt(m.rps)}`,
        `${m.p50ms.toFixed(2)}ms`,
        `${m.p99ms.toFixed(2)}ms`,
        pct,
      ]
      const cells2 = f === "nifra" ? cellVals.map((c) => `**${c}**`) : cellVals
      return `| ${cells2.join(" | ")} |`
    })
    .join("\n")
  return `${head}\n${body}`
}

function sectionBlock(runtime: string, results: Results): string {
  const cellsFor = (w: string): Record<string, Measure> => {
    const out: Record<string, Measure> = {}
    for (const f of Object.keys(results[runtime] ?? {})) {
      const m = results[runtime]?.[f]?.[w]
      if (m) out[f] = m
    }
    return out
  }
  const tables = WORKLOADS.map((w) => table(runtime, w, cellsFor(w))).join("\n\n")
  if (runtime !== "bun") return tables
  // Bun gets the explicit nifra-vs-Elysia ratio line (nifra's home-runtime competitor).
  const ratio = (w: string): string => {
    const nifra = results.bun?.nifra?.[w]?.rps ?? 0
    const elysia = results.bun?.elysia?.[w]?.rps ?? 0
    return nifra > 0 && elysia > 0 ? `${Math.round((nifra / elysia) * 100)}%` : "—"
  }
  const ratios = WORKLOADS.map((w) => `**${ratio(w)}** (\`${w}\`)`).join(", ")
  return `${tables}\n\nnifra vs Elysia (median): ${ratios}.`
}

function splice(doc: string, marker: string, content: string): string {
  const start = `<!-- BENCH:${marker}:START -->`
  const end = `<!-- BENCH:${marker}:END -->`
  const s = doc.indexOf(start)
  const e = doc.indexOf(end)
  if (s < 0 || e < 0 || e < s) {
    throw new Error(`BENCHMARKS.md is missing the ${start} / ${end} markers`)
  }
  return `${doc.slice(0, s + start.length)}\n${content}\n${doc.slice(e)}`
}

// ── run ─────────────────────────────────────────────────────────────────────────────────────────
const { runtime, runs, write } = parseArgs(process.argv.slice(2))
const runtimes = runtime ? [runtime] : ["bun", "node", "deno"]

const samples: Results[] = []
let meta: Meta | undefined
for (let i = 0; i < runs; i++) {
  process.stderr.write(`run ${i + 1}/${runs}…\n`)
  const { meta: m, results } = await runOnce(runtime)
  meta ??= m
  samples.push(results)
}
if (meta === undefined) throw new Error("no successful runs")

// Median across runs for every runtime/framework/workload cell.
const merged: Results = {}
for (const rt of runtimes) {
  merged[rt] = {}
  const frameworks = new Set<string>()
  for (const s of samples) for (const f of Object.keys(s[rt] ?? {})) frameworks.add(f)
  for (const f of frameworks) {
    const wl: Record<string, Measure> = {}
    for (const w of WORKLOADS) {
      const got = samples.map((s) => s[rt]?.[f]?.[w]).filter((m): m is Measure => m !== undefined)
      if (got.length > 0) {
        wl[w] = {
          rps: median(got.map((m) => m.rps)),
          p50ms: median(got.map((m) => m.p50ms)),
          p99ms: median(got.map((m) => m.p99ms)),
        }
      }
    }
    ;(merged[rt] as Record<string, Record<string, Measure>>)[f] = wl
  }
}

const metaLine = `_Median of **${runs}** full-matrix runs (each a median of ${meta.runs} × ${meta.durationS}s oha samples @ ${meta.connections} connections) · Bun ${meta.bun} · Node ${meta.node} · Deno ${meta.deno} · oha ${meta.oha}. Read the same-run ratios, not the absolutes._`

if (!write) {
  console.log(`\n${metaLine}\n`)
  for (const rt of runtimes) console.log(`### ${rt}\n\n${sectionBlock(rt, merged)}\n`)
  process.exit(0)
}

const benchFile = Bun.file(BENCHMARKS)
let doc = (await benchFile.exists()) ? await benchFile.text() : BENCHMARKS_TEMPLATE
doc = splice(doc, "META", metaLine)
for (const rt of runtimes) doc = splice(doc, rt, sectionBlock(rt, merged))
await Bun.write(BENCHMARKS, doc)
process.stderr.write(`updated ${BENCHMARKS}\n`)

// Push the Node HTTP numbers to the website's single source of truth (the landing reads it), so the
// published bars never drift from the latest run. Skipped when this run had no Node section — so a
// `bench:http bun` run leaves the site's Node slice intact rather than wiping it.
const httpSlice = httpSliceFromNode(merged.node as never)
if (httpSlice.length > 0) await writeSiteBench({ http: httpSlice })
